Cypress.Commands.add('login', (username = 'test', pwd = 'test1234!_') => {
  cy.get('[data-cy=username-field]')
    .type(username, { delay: 100 });
  cy.get('[data-cy=password-field]')
    .type(pwd, { delay: 100, log: false });
  cy.get('[data-cy=submit]')
    .click();
});